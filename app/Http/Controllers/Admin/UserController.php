    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $users = User::query()
            ->whereRaw('LOWER(nama) LIKE ? ', ['%' . strtolower($request->search ?: '') . '%'])
            ->paginate($request->show ?: 5)
            ->withQueryString()
            ->through(
                fn ($item) =>
                [
                    'id' => $item->id,
                    'nama' => $item->nama,
                    'no_hp' => $item->no_hp,
                    'email' => $item->email
                ]
            );

        return Inertia::render('Admin/User', ['users' => $users]);
    }

